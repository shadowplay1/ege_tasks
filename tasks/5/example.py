# алгоритм:
# 1. двоичная запись
# 2.1 если число чётное, добавить 11 в конец двоичной записи
# 2.2 иначе: добавить 00 в конец двоичной записи
# 3. результат - новое число

# найти наименьшее число, при котором
# результат равен 126 
for n in range(200):
    s = bin(n)[2:]

    if n % 2 == 0:
        s += '11'
    else:
        s += '00'
    
    r = int(s, 2)

    if r == 126:
        print(n)
        break
